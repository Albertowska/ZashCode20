package com.google.zashcode.model;

public class Book {

    private String id;

    private int score;

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
